// Helper functions
export const getProductById = (id) => {
  return dummyProducts.find(product => product.id === id);
};

export const getProductsByCategory = (category) => {
  if (category === 'all') return dummyProducts;
  return dummyProducts.filter(product => product.category === category);
};

export const getProductsBySeller = (sellerId) => {
  return dummyProducts.filter(product => product.sellerId === sellerId);
};

export const searchProducts = (query) => {
  const lowercaseQuery = query.toLowerCase();
  return dummyProducts.filter(product => 
    product.name.toLowerCase().includes(lowercaseQuery) ||
    product.description.toLowerCase().includes(lowercaseQuery) ||
    product.seller.toLowerCase().includes(lowercaseQuery) ||
    product.tags.some(tag => tag.toLowerCase().includes(lowercaseQuery))
  );
};

export const getProductsByPriceRange = (minPrice, maxPrice) => {
  return dummyProducts.filter(product => 
    product.price >= minPrice && product.price <= maxPrice
  );
};

export const getFeaturedProducts = (limit = 8) => {
  return dummyProducts
    .filter(product => product.rating >= 4.7)
    .slice(0, limit);
};

export const getNewArrivals = (limit = 6) => {
  return dummyProducts.slice(-limit);
};

export const getBestSellers = (limit = 6) => {
  return dummyProducts
    .sort((a, b) => b.reviews - a.reviews)
    .slice(0, limit);
};